package fr.gouv.vitam.common.model.logbook;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Logbook lifecycle model
 */
public class LogbookLifecycle extends LogbookEvent {

    @JsonProperty("_id")
    private String id;

    @JsonProperty("_tenant")
    private Integer tenant;

    @JsonProperty("_v")
    private Integer version;

    @JsonProperty("events")
    private List<LogbookEvent> events;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the tenant
     */
    public Integer getTenant() {
        return tenant;
    }

    /**
     * @param tenant the tenant to set
     */
    public void setTenant(Integer tenant) {
        this.tenant = tenant;
    }

    /**
     * @return the version
     */
    public Integer getVersion() {
        return version;
    }

    /**
     * @param version the version to set
     */
    public void setVersion(Integer version) {
        this.version = version;
    }

    /**
     * @return the events
     */
    public List<LogbookEvent> getEvents() {
        return events;
    }

    /**
     * @param events the events to set
     */
    public void setEvents(List<LogbookEvent> events) {
        this.events = events;
    }

}
